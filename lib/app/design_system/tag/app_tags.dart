import '../theme/theme_extension.dart';
import 'package:flutter/material.dart';

import 'base_app_tag.dart';

class DefaultSolidTag extends BaseAppTag {
  const DefaultSolidTag({
    super.key,
    required super.label,
    super.leading,
    super.trailing,
  });

  @override
  Color backgroundColor(BuildContext context) {
    return context.tagTheme.defaultTagSolid;
  }

  @override
  Color textColor(BuildContext context) {
    return context.tagTheme.solidTagForeground;
  }
}

